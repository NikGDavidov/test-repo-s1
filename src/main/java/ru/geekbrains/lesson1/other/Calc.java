package ru.geekbrains.lesson1.other;

/**
 * Здесь мы производим вычисления
 */

public class Calc {
    /**
     * 4 метода-основные арифметические действия
      */
    public static int add(int a, int b) {
        return a + b;
    }

    public static int deduct(int a, int b) {
        return a - b;
    }
    public static int mult(int a, int b) {
        return a * b;
    }

    public static int dev(int a, int b) {
        return a / b;
    }

}